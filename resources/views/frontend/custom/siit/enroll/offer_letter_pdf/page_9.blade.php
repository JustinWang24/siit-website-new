    <p style="font-weight: bold;font-style: italic;">Enrolment Requirements and Orientation Day</p>
    <p style="padding-left: 16px;">
        1. You must fulfil the visa requirements of the Australian Government. As a condition of enrolment, the
        student authorizes SIIT to check visa entitlements electronically via VEVO for the duration of enrolment on
        the Department of Immigration and Citizenship website:</p>
    <p style="padding-left: 16px;"><a href="http://www.homeaffairs.gov.au/busi/visas-and-migration/visa-entitlement-verification-online-(vevo)" target="_blank">http://www.homeaffairs.gov.au/busi/visas-and-migration/visa-entitlement-verification-online-(vevo)</a></p>
    <p style="padding-left: 16px;">
        2.	You will be invited to attend our Orientation Day, organised to provide you with all the information required
        for you to be able to complete the course.<br/>
    </p>
    <p style="font-weight: bold;">Please note, it is compulsory for all students to attend Orientation Day!!</p>
    <p style="padding-left: 16px;">
        3.	You must provide SIIT with your residential address, contact phone number and email address as well as
        emergency contacts in Australia and Overseas. You MUST notify SIIT of any change in your contact
        details, including residential address, postal address (if applicable), email address, phone number and
        emergency contacts within 10 days of the change occurring.
    </p>
    <p style="padding-left: 16px;">
        4.	The course fee is payable to secure your place in the course(s). To secure your place in the course, the
        fees must be paid at least 14 days before the course commences. This fee consists of the following:
        <ul style="padding-left: 1em;">
            <li>The enrolment/administration fee.</li>
            <li>The course materials fee.</li>
            <li>The tuition fee.</li>
            <li>The fee for Overseas Student Health Cover (OSHC) for overseas students, if applicable.</li>
        </ul>
    </p>
    <p style="padding-left: 16px;">
        5.	You must provide all original and/or certified copies of documents and transcripts on Orientation Day. A
        letter or email will be sent to you to advise you of the documents required to finalise your enrolment.
    </p>
