    <br/>
    <p>If the student has been advised that they are “Not Yet Competent”, but they believe that they genuinely do have
        the required degree of competency and that they have provided reasonable proof of this to SIIT, the student will
        have to follow the right appeal process.</p>
    <p>The process is quite simple, and is allowed by SIIT to ensure that all students are fully satisfied with the fairness
        and accuracy of our assessment processes, as follows:</p>
    <ul>
        <li>The student should firstly discuss the matter with their appointed assessor. If they are still not satisfied,
            the student may then, have the matter referred to the CEO and/or the Academic Manager for
            consideration.</li>
        <li>The student must complete the “Complaint and Appeal form” and ensure that they provide sufficient
            details about themselves and the course, and the circumstances surrounding the concern, complaint or
            appeal, who was involved, any appropriate evidence and witnesses etc.</li>
        <li>The CEO and/or the Academic Manager will contact the student within 24 hours to confirm receipt of this
            form and to schedule a formal face-to-face meeting with the student. This meeting will take place within 5
            working days or earlier from the date of receipt of the complaint or appeal application.</li>
        <li style="font-weight: bold;">The student will also be given an option to be accompanied or assisted by a support person
            during this or any other meetings relating to the matter(s).</li>
        <li>The CEO and/or the Academic Manager will discuss the circumstances with the trainer/assessor or any
            other staff member and make an informed decision.</li>
        <li>The student will be contacted with the result within 10 working days from the lodgement of Complaint or
            Appeal form, the student has further 5 working days to respond to a formal decision.</li>
        <li>A written statement of the appeal outcome, including reasons for the decision will also be documented
            and provided to the student via email or face-to-face.</li>
        <li>A record of this written statement will also be filed in the student’s admin folder. Furthermore, if internal
            appeal process results in an outcome that supports the student, SIIT will take appropriate steps and will
            keep the student informed about the course of action in this regard.</li>
        <li style="font-weight: bold;">Where a complaint, concern, or appeal cannot be resolved through discussion and conciliation,
            SIIT acknowledges the need for a student to go for external appeal process (refer to external
            agency section for more details regarding this).</li>
        <li style="font-weight: bold;">If external appeal process results in an outcome that supports the student, SIIT will take
            appropriate steps and will keep the student informed about the course of action in this regard.</li>
        <li style="font-weight: bold;">SIIT will maintain the student’s enrolment whilst the complaints and appeals process is ongoing.</li>
    </ul>
    <p><strong>Note:</strong> SIIT will accept an appeal against an assessment decision for a period of no longer than 2 months after
        the assessment decision date.</p>
    <br/>
    <p><strong>External Agency (for International Students)</strong></p>
    <p>If a student is not happy with the internal appeal outcome, the college will refer them to the Overseas Students
        Ombudsman (OSO) .</p>
    <p>OSO investigates complaints about problems that overseas students or intending overseas students may have
        with private education and training in Australia.</p>
