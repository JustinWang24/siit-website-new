    <p>3. You must provide SIIT with your residential address, contact phone number and email address as well as emergency contacts in Australia and Overseas. You MUST notify SIIT of any change in your contact details, including residential address, postal address (if applicable), email address, phone number and emergency contacts within 10 days of the change occurring.</p>
