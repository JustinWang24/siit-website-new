    <h3>Student Academic Progress and Attendance</h3>
    <p>As part of the Orientation session, you will be provided with a training schedule which you will follow in order to ensure completion of the course within the timeline we have agreed on. Please, ensure you familiarize yourself with the schedule and due dates of your assessments.
    </p>
    <p>SIIT will follow the Department of Immigration and Border Protection policy and procedure for systematically monitoring students’ course progress. SIIT will be proactive in notifying and counselling students who are at risk of failing to meet course progress requirements. SIIT will report students, under section 19 of the ESOS Act, who have breached the course progress requirements.</p>
    <p>SIIT believes that regular and sufficient class attendance is necessary for successful achievement of expected outcomes in VET.</p>
    <p>Therefore, SIIT will also record and monitor students’ class attendance, and may remind students of their student visa responsibilities.</p>
    <p>You are expected to attend 20 hours of training per week (15 hours classroom delivery and a minimum of 5 hours of online learning).</p>
    <p>
        <strong style="text-decoration: underline;">It is your responsibility to attend a minimum of 80% of classes</strong> to ensure you absorb all the information provided by the trainers as per the course schedule. Trainers will not interrupt the session flow to provide students with individual tuition due to the student not attending classes. Should it be required, the student should endeavour to catch up with the information taught in class by consulting with the trainer outside training sessions or by working together with other students on projects or in their own time.
    </p>
    <p>You can find SIIT’s Course Progress and Intervention policy in our Student Handbook advising students of:</p>
