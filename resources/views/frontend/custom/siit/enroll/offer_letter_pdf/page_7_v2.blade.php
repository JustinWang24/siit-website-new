    <h3>Enrolment Requirements and Orientation Day</h3>
    <p>1. You must fulfil the visa requirements of the Australian Government. As a condition of enrolment, the student authorizes SIIT to check visa entitlements electronically via VEVO for the duration of enrolment on the Department of Immigration and Citizenship website:
