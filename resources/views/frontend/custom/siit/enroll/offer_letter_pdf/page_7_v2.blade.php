    <h3>Transfer between Registered Providers</h3>
    <p>Under Standard 7 of the National Code 2018, Registered Providers must not knowingly enroll a Student wishing to transfer from another Registered Provider’s course prior to the Student completing six calendar months of his /her principal course of study, except in limited circumstances as outlined in the policy.
    </p><p>SIIT is entitled to determine the circumstances in which it will provide or refuse to provide a Release Letter. Where a Student requests a transfer within the period of six months of commencement of their principal course, SIIT will assess the request for transfer against the policy.
    </p><p>No Release Letter is required where:</p>

    <p>1. the Student is government sponsored and that government sponsor provides written support for the change as it considers the change to be in the Student's best interests; or
    </p><p>2. SIIT has ceased to be registered or the course in which the Student is enrolled has ceased to be registered; or
    </p><p>3. SIIT has a sanction imposed on it that prevents the Student from continuing their principal course.
    </p><p>For more information on this matter, you can find our Student Transfer Policy and Procedure on our website at the following link: {{ url('/page/assessments-and-policy') }}</p>

    <h3>Deferment, Suspension and Cancellation Policy</h3>
    <p>It is the policy of SIIT and the student’s responsibility to complete the course within the agreed course duration. However, unforeseen and unexpected circumstances may occur beyond the student’s control which can affect their ability to complete their course.
    </p><p>SIIT may defer a student’s commencement on the following grounds:</p>
    <ul><li>when a course is not offered</li></ul>
    <p>SIIT may suspend a student enrolment due to the following instances:</p>
    <ul>
        <li>when a student is deemed to be in breach of the Student Code of Conduct</li>
        <li>
            when a student is deemed to be not making satisfactory course progress and fails to comply with the requirements of the Intervention Plan</li>
    </ul>
    <p>
        SIIT may cancel a student enrolment due to the following instances:
    </p>
    <ul>
    <li>when a student demonstrates serious breach of the Student Code of Conduct
    </li><li>when a student is in breach of the course progress policy
    </li><li>when a student is continually absent from scheduled course hours
    </li><li>non-payment of outstanding fees</li>
    </ul>

    <p>In cases where suspension or cancellation of the student's enrolment is initiated by SIIT, students will be notified and given 20 working days to access SIIT’s internal complaints and appeals process.</p>
    <p>Students may initiate the deferral, suspension and cancellation of enrolment, including granting of a leave of absence during the course through formal agreement on the following limited circumstances:</p>

    <ul>
        <li>
        compassionate or compelling circumstances
        </li><li>where students do not receive their visa in time
        </li><li>serious illness or injury – where a medical certificate states the student was unable to attend classes.
        </li><li>bereavement of close family members
        </li><li>major political upheaval or natural disaster in home country
        </li><li>a traumatic experience</li>
    </ul>

    <p>Please note: Deferring to go home to be married or attend the wedding of a family member is not considered to be compassionate or compelling circumstances which is beyond the control of the student.</p>
    <p>You can find our “Deferral, Suspension and Cancellation policy and procedure” in our Student Handbook as well as on our website at the following link: {{ url('/page/assessments-and-policy') }}</p>

