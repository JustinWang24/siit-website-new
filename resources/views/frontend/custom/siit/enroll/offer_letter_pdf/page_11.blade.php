    <p>As completion of assessments relates to your course progress and to you successfully completing your
        qualification, you MUST complete your assignments as outlined in your timetable and as instructed by your
        trainer/assessor. It is vital that you <span style="font-style: italic;text-decoration: underline;">submit your assessments on or before the due date</span> to allow time for
