    <p style="font-weight: bold;">Transfer between Registered Providers</p>
    <br/>
    <p>Under Standard 7 of the National Code 2018, Registered Providers must not knowingly enroll a Student wishing
        to transfer from another Registered Provider’s course prior to the Student completing six calendar months of his
        /her principal course of study, except in limited circumstances as outlined in the policy.</p>
    <p>SIIT is entitled to determine the circumstances in which it will provide or refuse to provide a Release Letter.
        Where a Student requests a transfer within the period of six months of commencement of their principal course,
        SIIT will assess the request for transfer against the policy.</p>
    <p>No Release Letter is required where:</p>
    <ol>
        <li>the Student is government sponsored and that government sponsor provides written support for the
            change as it considers the change to be in the Student's best interests; or</li>
        <li>SIIT has ceased to be registered or the course in which the Student is enrolled has ceased to be
            registered; or</li>
        <li>SIIT has a sanction imposed on it that prevents the Student from continuing their principal course.</li>
    </ol>
    <p>For more information on this matter, you can find our Student Transfer Policy and Procedure on our website at
        the following link: <a href="{{ url('/page/SIIT-policy-and-procedures') }}" target="_blank">{{ url('/page/SIIT-policy-and-procedures') }}</a></p>
    <br>
    <p style="font-weight: bold;">
        Deferment, Suspension and Cancellation Policy
    </p>
    <p>It is the policy of SIIT and the student’s responsibility to complete the course within the agreed course duration. However, unforeseen and unexpected circumstances may occur beyond the student’s control which can affect their ability to complete their course.
    </p>
    <p>SIIT may defer a student’s commencement on the following grounds:</p>
    <p style="padding-left: 16px;">-	when a course is not offered
    </p>
    <p>SIIT may suspend a student enrolment due to the following instances:
    </p>
    <p style="padding-left: 16px;">	when a student is deemed to be in breach of the Student Code of Conduct
    </p>
    <p style="padding-left: 16px;">-	when a student is deemed to be not making satisfactory course progress and fails to comply with the requirements of the Intervention Plan
    </p>
    <p>SIIT may cancel a student enrolment due to the following instances:
    </p>
    <p style="padding-left: 16px;">-	when a student demonstrates serious breach of the Student Code of Conduct
    </p>
    <p style="padding-left: 16px;">-	when a student is in breach of the course progress policy</p>
    <p style="padding-left: 16px;">-	when a student is continually absent from scheduled course hours</p>
    <p style="padding-left: 16px;">-	non-payment of outstanding fees</p>
