    <p style="text-decoration: underline;"><strong>Please note:</strong></p>
    <ol>
        <li>
            <p>If the student is able to demonstrate compassionate or compelling circumstances, the amount refunded to the student can be higher than the one specified in the table above at SIIT’s discretion.</p>
            <p><strong style="font-style: italic;">'Compassionate or compelling circumstances'</strong> are reasons why a student’s enrolment status may be changed. These are generally those situations beyond the control of the student. The following are examples of compassionate or compelling circumstances:
            </p>
            <p style="padding-left: 16px;">a.	Unavailability of units in the enrolled course (by SIIT);</p>
            <p style="padding-left: 16px;">b.	Inability to commence study on commencement date due to student visa delay or refusal (evidence from DIBP must be provided. This clause does not include the situation where the student has decided not to seek extension of visa for whatever reason.);</p>
            <p style="padding-left: 16px;">c.	Serious illness or injury, (a medical certificate must be provided stating that student is unable to attend class);</p>
            <p style="padding-left: 16px;">d.	Bereavement of close family members such as parents, grandparents, brother, sister, (a death certificate must be provided);</p>
            <p style="padding-left: 16px;">e.	A traumatic experience such as witnessing a serious accident or witnessing or being victim of a serious crime (police report must be provided);</p>
            <p style="padding-left: 16px;">f.	A major political upheaval in the student’s home country which requires emergency travel that will affect student studies;</p>
            <p style="padding-left: 16px;">g.	A natural disaster in the student’s home country which requires emergency travel that will affect student studies;</p>
        </li>
        <li>This refund is applicable to all types of students (overseas/international and domestic).</li>
        <li>If the student is not happy with SIIT refund policy or its complaint and appeal policy, the student has the right to seek external advice and pursue its action under the Australia’s consumer protection laws.</li>
        <li>SIIT will only refund any monies due to the student in the student’s nominated bank account or to the student in person.</li>
    </ol>
